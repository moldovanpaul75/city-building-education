DROP DATABASE IF EXISTS city_building;
CREATE DATABASE IF NOT EXISTS city_building;
USE city_building;


CREATE TABLE IF NOT EXISTS city(
	id INT NOT NULL AUTO_INCREMENT,
    `name` VARCHAR(50) NOT NULL,
    latitude NUMERIC(10,6) NOT NULL,
	longitude NUMERIC(10,6) NOT NULL,
    PRIMARY KEY(id)
);

CREATE TABLE IF NOT EXISTS district(
	id INT NOT NULL AUTO_INCREMENT,
    city INT NOT NULL,
    `name` VARCHAR(50) NOT NULL,
	xStart INT NOT NULL,
    xEnd INT NOT NULL,
    yStart INT NOT NULL,
    yEnd INT NOT NULL,
    PRIMARY KEY(id),
    UNIQUE(xStart, xEnd, yStart, yEnd),
    FOREIGN KEY (city) REFERENCES city(id)
);


CREATE TABLE IF NOT EXISTS layerType(
	id INT NOT NULL AUTO_INCREMENT,
    `name` VARCHAR(30) NOT NULL,
    PRIMARY KEY(id)
);

CREATE TABLE IF NOT EXISTS tileType(
	id INT NOT NULL AUTO_INCREMENT,
    layer INT NOT NULL,
    `name` VARCHAR(30) NOT NULL,
    rating INT NOT NULL,
    pollutionLevel INT NOT NULL,
	iconPath VARCHAR(100) NOT NULL,
    PRIMARY KEY(id),
    FOREIGN KEY (layer) REFERENCES layerType(id)
);

CREATE TABLE IF NOT EXISTS contact(
	id INT NOT NULL AUTO_INCREMENT,
    phone VARCHAR(10) NOT NULL UNIQUE,
    email VARCHAR(30) NOT NULL,
    website VARCHAR(30) NULL,
    PRIMARY KEY(id)
);

CREATE TABLE IF NOT EXISTS address(
	id INT NOT NULL AUTO_INCREMENT,
    address VARCHAR(100) NOT NULL,
    zipcode VARCHAR(6) NOT NULL,
	PRIMARY KEY(id)
);

CREATE TABLE IF NOT EXISTS tileInfo(
	id INT NOT NULL AUTO_INCREMENT,
    capacity INT NOT NULL,
    availability INT NOT NULL,
    address INT NULL,
    contact INT NULL,
    PRIMARY KEY(id),
    FOREIGN KEY (contact) REFERENCES contact(id),
    FOREIGN KEY (address) REFERENCES address(id)
);

CREATE TABLE IF NOT EXISTS tile(
	id INT NOT NULL AUTO_INCREMENT,
    district INT NULL,
    tileType INT NOT NULL,
    tileInfo INT NULL,
    xCoord INT NOT NULL,
    yCoord INT NOT NULL,
    PRIMARY KEY(id),
    UNIQUE(xCoord, yCoord),
    KEY (xCoord, yCoord),
    FOREIGN KEY (tileType) REFERENCES tileType(id),
    FOREIGN KEY (tileInfo) REFERENCES tileInfo(id),
    FOREIGN KEY (district) REFERENCES district(id)
);


